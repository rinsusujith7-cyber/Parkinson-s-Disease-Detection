# ── Residual block ────────────────────────────────────────────────────────────
def res_block(x, filters):
    sc = x
    x  = layers.Conv2D(filters, 3, padding='same', use_bias=False)(x)
    x  = layers.BatchNormalization()(x); x = layers.ReLU()(x)
    x  = layers.Conv2D(filters, 3, padding='same', use_bias=False)(x)
    x  = layers.BatchNormalization()(x)
    if sc.shape[-1] != filters:
        sc = layers.Conv2D(filters, 1, padding='same', use_bias=False)(sc)
        sc = layers.BatchNormalization()(sc)
    return layers.ReLU()(layers.Add()([x, sc]))

def build_mri_cnn(input_shape=(128, 128, 1), num_classes=2):
    inp = layers.Input(shape=input_shape)
    x   = layers.Conv2D(32, 7, padding='same', use_bias=False)(inp)
    x   = layers.BatchNormalization()(x); x = layers.ReLU()(x)
    x   = layers.MaxPooling2D(3, strides=2, padding='same')(x)
    x   = res_block(x,  32); x = layers.MaxPooling2D(2)(x)
    x   = res_block(x,  64); x = layers.MaxPooling2D(2)(x)
    x   = res_block(x, 128); x = layers.MaxPooling2D(2)(x)
    x   = res_block(x, 256)
    x   = layers.GlobalAveragePooling2D()(x)
    x   = layers.Dense(256, activation='relu',
                       kernel_regularizer=regularizers.l2(1e-4))(x)
    x   = layers.Dropout(0.5)(x)
    out = layers.Dense(num_classes, activation='softmax')(x)
    return models.Model(inp, out, name='MRI_ResNet')

cnn_model = build_mri_cnn()
