# ── Per-subject aggregation ────────────────────────────────────────────────────
def compute_genomic_features(grp):
    total     = len(grp)
    iso_only  = (grp['IN_FILE'] == '1').sum()
    ppmi_only = (grp['IN_FILE'] == '2').sum()
    both      = (grp['IN_FILE'] == 'B').sum()
    conflict  = (grp['IN_FILE'] == 'O').sum()
    snv       = (grp['var_class'] == 'SNV').sum()
    indel     = (grp['var_class'] == 'INDEL').sum()
    return pd.Series({
        'total_variants'      : total,
        'iso_only_count'      : iso_only,
        'ppmi_only_count'     : ppmi_only,
        'both_count'          : both,
        'conflict_count'      : conflict,
        'snv_count'           : snv,
        'indel_count'         : indel,
        'iso_only_pct'        : iso_only / (total + 1e-6),
        'ppmi_only_pct'       : ppmi_only / (total + 1e-6),
        'both_pct'            : both / (total + 1e-6),
        'conflict_pct'        : conflict / (total + 1e-6),
        'snv_pct'             : snv / (total + 1e-6),
        'indel_pct'           : indel / (total + 1e-6),
        'private_variant_ratio': (iso_only + ppmi_only) / (total + 1e-6),
        'concordance_rate'    : (both) / (iso_only + ppmi_only + both + 1e-6),
    })

