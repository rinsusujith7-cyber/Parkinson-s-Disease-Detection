import matplotlib.cm as cm_module

def get_gradcam(model, img_input, last_conv_name, class_idx=None):
    """Grad-CAM for single-input or multi-input model (uses image branch)."""

    # Build grad model using image input only
    grad_model = tf.keras.models.Model(
        inputs  = model.input,
        outputs = [model.get_layer(last_conv_name).output, model.output]
    )
    with tf.GradientTape() as tape:
        conv_out, preds = grad_model(img_input)
        if class_idx is None:
            class_idx = tf.argmax(preds[0])
        loss = preds[:, class_idx]
    grads   = tape.gradient(loss, conv_out)
    pooled  = tf.reduce_mean(grads, axis=(0,1,2))
    hm      = conv_out[0] @ pooled[..., tf.newaxis]
    hm      = tf.squeeze(hm); hm = tf.nn.relu(hm)
    hm      = hm / (tf.reduce_max(hm)+1e-8)
    return hm.numpy(), preds.numpy()[0]

def overlay(img_2d, hm, alpha=0.55):
    H, W = img_2d.shape
    hm_r = np.array(tf.image.resize(hm[...,np.newaxis],[H,W])).squeeze()
    rgb  = np.stack([img_2d]*3, axis=-1)
    col  = cm_module.get_cmap('jet')(hm_r)[:,:,:3]
    return np.clip(alpha*col + (1-alpha)*rgb, 0, 1), hm_r

