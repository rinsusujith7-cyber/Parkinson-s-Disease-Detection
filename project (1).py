def load_mri_images_from_subfolders(base_folder, img_size=IMG_SIZE):
    images, labels, fnames = [], [], []

    # Define subfolders containing images based on the ls output
    image_subfolders = ['train/images', 'test/images', 'valid/images']

    for subfolder in image_subfolders:
        current_folder = os.path.join(base_folder, subfolder)
        # Look for .jpg files as per ls output
        for p in sorted(glob.glob(os.path.join(current_folder, '*.jpg'))):
            fname = os.path.basename(p)
            label = None # Initialize label as None
            # Classify based on filename prefix
            if 'NoneDem' in fname:
                label = 0  # Normal
            elif 'MildDem' in fname or 'VeryMildDem' in fname:
                label = 1  # Parkinson (or general Dementia)

            if label is not None: # Only process if a valid label was found
                img = Image.open(p).convert('L')
                img = img.resize((img_size, img_size), Image.LANCZOS)
                images.append(np.array(img))
                labels.append(label)
                fnames.append(fname)

    return images, labels, fnames

