def build_fusion_model(img_shape=(128,128,1), gen_dim=8, num_classes=2):
    # --- Image branch (lightweight CNN) ---
    img_in = layers.Input(shape=img_shape, name='mri_input')
    x = layers.Conv2D(32, 3, padding='same', activation='relu')(img_in)
    x = layers.MaxPooling2D(2)(x)
    x = layers.Conv2D(64, 3, padding='same', activation='relu')(x)
    x = layers.BatchNormalization()(x); x = layers.MaxPooling2D(2)(x)
    x = layers.Conv2D(128,3, padding='same', activation='relu')(x)
    x = layers.BatchNormalization()(x); x = layers.MaxPooling2D(2)(x)
    x = layers.GlobalAveragePooling2D()(x)
    x = layers.Dense(128, activation='relu')(x)
    x = layers.Dropout(0.4)(x)

    # --- Genomic branch (MLP) ---
    gen_in = layers.Input(shape=(gen_dim,), name='genomic_input')
    g = layers.Dense(64, activation='relu')(gen_in)
    g = layers.BatchNormalization()(g)
    g = layers.Dense(32, activation='relu')(g)
    g = layers.Dropout(0.3)(g)

    # --- Fusion ---
    fused = layers.Concatenate(name='multimodal_fusion')([x, g])
    fused = layers.Dense(128, activation='relu')(fused)
    fused = layers.Dropout(0.4)(fused)
    fused = layers.Dense(64,  activation='relu')(fused)
    out   = layers.Dense(num_classes, activation='softmax')(fused)
    return models.Model(inputs=[img_in, gen_in], outputs=out, name='MultiModal_Fusion')

