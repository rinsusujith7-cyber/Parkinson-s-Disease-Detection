def dice_bce(y_true, y_pred, sm=1e-6):
    y_t=tf.reshape(y_true,[-1]); y_p=tf.reshape(y_pred,[-1])
    inter=tf.reduce_sum(y_t*y_p)
    dice=1-(2*inter+sm)/(tf.reduce_sum(y_t)+tf.reduce_sum(y_p)+sm)
    return tf.keras.losses.binary_crossentropy(y_true, y_pred) + dice

