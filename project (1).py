# ── U-Net ─────────────────────────────────────────────────────────────────────
def cblk(x, f):
    for _ in range(2):
        x = layers.Conv2D(f, 3, padding='same', use_bias=False)(x)
        x = layers.BatchNormalization()(x); x = layers.ReLU()(x)
    return x

def build_unet(input_shape=(128, 128, 1)):
    inp = layers.Input(shape=input_shape)
    e1=cblk(inp,32);  p1=layers.MaxPooling2D(2)(e1)
    e2=cblk(p1, 64);  p2=layers.MaxPooling2D(2)(e2)
    e3=cblk(p2,128);  p3=layers.MaxPooling2D(2)(e3)
    e4=cblk(p3,256);  p4=layers.MaxPooling2D(2)(e4)
    b =cblk(p4,512)
    u4=layers.UpSampling2D(2)(b);  u4=layers.Concatenate()([u4,e4]); d4=cblk(u4,256)
    u3=layers.UpSampling2D(2)(d4); u3=layers.Concatenate()([u3,e3]); d3=cblk(u3,128)
    u2=layers.UpSampling2D(2)(d3); u2=layers.Concatenate()([u2,e2]); d2=cblk(u2,64)
    u1=layers.UpSampling2D(2)(d2); u1=layers.Concatenate()([u1,e1]); d1=cblk(u1,32)
    out=layers.Conv2D(1, 1, activation='sigmoid')(d1)
    return models.Model(inp, out, name='UNet_PPMI')

